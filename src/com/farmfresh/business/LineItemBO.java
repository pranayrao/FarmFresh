package com.farmfresh.business;

public class LineItemBO {

	public String lineItemDescription;
	public String lineItemId;
	public String lineItemQty;
	public String lineOrderId;
	
	public String getLineItemDescription() {
		return lineItemDescription;
	}
	public void setLineItemDescription(String lineItemDescription) {
		this.lineItemDescription = lineItemDescription;
	}
	public String getLineItemId() {
		return lineItemId;
	}
	public void setLineItemId(String lineItemId) {
		this.lineItemId = lineItemId;
	}
	public String getLineItemQty() {
		return lineItemQty;
	}
	public void setLineItemQty(String lineItemQty) {
		this.lineItemQty = lineItemQty;
	}
	public String getLineOrderId() {
		return lineOrderId;
	}
	public void setLineOrderId(String lineOrderId) {
		this.lineOrderId = lineOrderId;
	}
	
}
